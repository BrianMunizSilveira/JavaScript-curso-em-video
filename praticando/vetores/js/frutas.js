let frutas = ["Maçã", "Morango", "Mamão"] // Criando um array 'frutas' com os valores ["Maçãm", "Morango", "Mamão"].

// Adicionando "Manga" no final do Array
frutas.push("Manga")

// Removendo o primeiro elemento do Array
frutas.shift()

